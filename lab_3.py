# Функція корисності
def utility(x):
    return 0.15 * (x ** 2)

# Дано
initial_capital = 150000

# Варіант 1: Державні цінні папери (безризиковий варіант)
fixed_profit = initial_capital * 1.05
fixed_utility = utility(fixed_profit)

# Варіант 2: Корпоративні акції (ризикований варіант)
success_profit = initial_capital * 1.20
failure_profit = initial_capital
expected_utility = 0.7 * utility(success_profit) + 0.3 * utility(failure_profit)

# Премія за ризик
risk_premium = fixed_utility - expected_utility

# Висновки та результати
print(f"Корисність від державних цінних паперів (безризиковий варіант): {fixed_utility}")
print(f"Очікувана корисність від акцій корпорацій (ризикований варіант): {expected_utility}")
print(f"Премія за ризик: {risk_premium}")

if fixed_utility > expected_utility:
    print("Краще вкласти у державні цінні папери (безризиковий варіант).")
else:
    print("Краще вкласти в акції корпорацій (ризикований варіант).")

# Розкриття економічної суті премії за ризик
if risk_premium > 0:
    print("\nЕкономічна суть: Підприємство віддає перевагу стабільному прибутку, навіть якщо ризиковані варіанти обіцяють більший прибуток. Позитивна премія за ризик вказує на те, що керівництво готове пожертвувати потенційними прибутками, щоб уникнути невизначеності.")
else:
    print("\nЕкономічна суть: Підприємство готове ризикувати, оскільки очікувана корисність ризикованих варіантів перевищує стабільний прибуток. Відсутність премії за ризик свідчить про схильність до ризику.")

# Приклад підприємства
print("\nПриклад підприємства з подібною функцією корисності: Це може бути державний пенсійний фонд або консервативна інвестиційна компанія, яка прагне уникнути ризику, зберігаючи капітал і отримуючи гарантований прибуток.")
